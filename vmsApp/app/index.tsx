import { useAuth } from '@/contexts/AuthContext';
import { useRouter } from 'expo-router';
import { LogIn } from 'lucide-react-native';
import AsyncStorage from '@react-native-async-storage/async-storage';
const API_URL = 'http://localhost:5000/api/auth/teacher-login';
export default function LoginScreen() {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');
  const [loading, setLoading] = useState(false);
  const { signIn, user } = useAuth();
  const router = useRouter();

  // if (user) {
  //   router.replace('/(tabs)');
  //   return null;
  // }

  // const handleLogin = async () => {
  //   setError('');
  //   setLoading(true);

  //   const { error: signInError } = await signIn(email, password);

  //   if (signInError) {
  //     setError(signInError);
  //     setLoading(false);
  //   } else {
  //     router.replace('/(tabs)');
  //   }
  // };
  const handleLogin = async () => {
    setError('');
    try {
      const res = await fetch(API_URL, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email, password }),
      });
      const data = await res.json();

      if (!res.ok) throw new Error(data.error || 'Login failed');

      await AsyncStorage.setItem('token', data.token);
      await AsyncStorage.setItem('teacher_id', data.user.id);
      await AsyncStorage.setItem('teacher_name', data.user.full_name);
      await AsyncStorage.setItem('school_id', data.user.school_id);
      await AsyncStorage.setItem('user', JSON.stringify(data.user));
      router.replace('/(tabs)');
    } catch (err: any) {
      setError(err.message);
    }
  };
  return (
    <KeyboardAvoidingView
      behavior={Platform.OS === 'ios' ? 'padding' : 'height'}
      style={styles.container}
    >
      <View style={styles.content}>
        <View style={styles.header}>
          <LogIn size={48} color="#3B82F6" />
          <Text style={styles.title}>Gestion des Violations</Text>
          <Text style={styles.subtitle}>Connexion Enseignant</Text>
        </View>

        <View style={styles.form}>
          <View style={styles.inputGroup}>
            <Text style={styles.label}>Email</Text>
            <TextInput
              style={styles.input}
              placeholder="votre@email.com"
              value={email}
              onChangeText={setEmail}
              autoCapitalize="none"
              keyboardType="email-address"
              editable={!loading}
            />
          </View>

          <View style={styles.inputGroup}>
            <Text style={styles.label}>Mot de passe</Text>
            <TextInput
              style={styles.input}
              placeholder="••••••••"
              value={password}
              onChangeText={setPassword}
              secureTextEntry
              editable={!loading}
            />
          </View>

          {error ? <Text style={styles.errorText}>{error}</Text> : null}

          <TouchableOpacity
            style={[styles.button, loading && styles.buttonDisabled]}
            onPress={handleLogin}
            disabled={loading}
          >
            {loading ? (
              <ActivityIndicator color="#FFF" />
            ) : (
              <Text style={styles.buttonText}>Se connecter</Text>
            )}
          </TouchableOpacity>
        </View>
      </View>
    </KeyboardAvoidingView>
  );
}

